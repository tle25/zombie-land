     // Draw walls  
     public void draw(Graphics2D g){

         for(int i = 0; i < walls.length ; i++)
             for(int j = 0 ; j < walls[i].length; j++){      
                 // Draw breakable walls
                 // Assign values 3,4,5 are the hidden items inside the walls
                 // Whenever these walls are break, items will be displayed
              if(walls[i][j] == 1 || walls[i][j] == 3 ||
                  walls[i][j] == 4 || walls[i][j] == 5){
              g.drawImage(BreakableWall, i*wallsize, j*wallsize, wallsize, wallsize, null); 
              }
              
              
              // Draw unbreakble walls
              else if(walls[i][j] == 2){
              g.drawImage(UnBreakableWall, i*wallsize, j*wallsize, wallsize, wallsize, null); 
              }
              
              
              
              // Display PowerUp items after a breakable wall is destroyed
             else if(walls[i][j] >= 7){
                  switch (walls[i][j]) {
                      case 7:
                          g.drawImage(Controller.power.poisonBullet(), i*wallsize, j*wallsize,wallsize,wallsize,null);
                          break;
                      case 8:
                          g.drawImage(Controller.power.Shield(), i*wallsize, j*wallsize,wallsize,wallsize,null);
                          break;
                      case 9:
                          g.drawImage(Controller.power.FreezingBullet(), i*wallsize, j*wallsize,wallsize,wallsize,null);
                          break;
                      default:
                        //  break;
                  }
              }

        } 
     
    }
     
    
